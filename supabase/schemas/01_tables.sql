-- Polymorphic interaction timeline (AD-13 target_type/target_id pattern, reused
-- rather than forked). ONE surface serves both "notes" and "timeline": a note is
-- kind='note'; the timeline is this table ordered by created_at.
--
-- AD-3/F3 visibility: this table has NO own visibility column. When an
-- interaction concerns a specific shidduch-scoped conversation it carries
-- reference_link_id, and RLS derives visibility by walking
-- reference_links -> shidduchim.visibility. Interactions with no shidduch context
-- (e.g. "updated phone number") are account-scoped only. `kind` is a closed
-- enumeration precisely so a coarse aggregate ("N of M references contacted",
-- FR68) can be computed WITHOUT reading candid row content.
create table public.interactions (
    id bigint generated by default as identity primary key,
    account_id bigint not null,
    created_at timestamp with time zone not null default now(),
    target_type text not null,
    target_id bigint not null,
    -- Which parent this row derives its visibility from. NOT a visibility column
    -- (AD-3 forbids those): it stores no visibility decision, it declares which
    -- parent the answer is derived FROM, and the constraint below makes the two
    -- cases total and mutually exclusive.
    --
    --   'shidduch' -> reference_link_id is required; visibility derives by
    --                 joining reference_links -> shidduchim.
    --   'account'  -> no shidduch parent exists, so there is no shidduch whose
    --                 visibility could ever make this row child-visible. The
    --                 future child policy denies this bucket wholesale.
    --
    -- Without this discriminator a row with a null reference_link_id silently
    -- fell through BOTH branches, which is how a free-text note would have
    -- leaked to a candidate regardless of the parent's pipeline state.
    scope text not null default 'account',
    reference_link_id bigint,
    actor_member_id bigint,
    kind text not null default 'note',
    body text,
    metadata jsonb,
    constraint interactions_target_type_check check (
        target_type in ('reference', 'shidduch')
    ),
    constraint interactions_kind_check check (
        kind in ('note', 'call_logged', 'status_change', 'merge', 'link_created', 'link_removed')
    ),
    constraint interactions_scope_check check (scope in ('shidduch', 'account')),
    -- The structural half of AD-3, enumerated exhaustively. scope = 'shidduch'
    -- means "visibility derives from a parent shidduch", and which shidduch
    -- depends on what the row is about:
    --   target_type = 'reference' -> via reference_link_id -> the link's shidduch
    --   target_type = 'shidduch'  -> target_id IS the shidduch, so no link
    -- scope = 'account' means there is no shidduch parent at all, and is only
    -- legal for reference-targeted rows: an interaction ABOUT a shidduch always
    -- has that shidduch as its parent, so it can never be account-scoped.
    constraint interactions_scope_link_check check (
        (scope = 'shidduch' and target_type = 'reference' and reference_link_id is not null)
        or (scope = 'shidduch' and target_type = 'shidduch' and reference_link_id is null)
        or (scope = 'account' and target_type = 'reference' and reference_link_id is null)
    )
);

