    id bigint generated by default as identity primary key,
    created_at timestamp with time zone not null default now(),
    name text not null default 'My Account',
    transparency_level text not null default 'shared',
    data_region text,
    -- Billing (AD-16) — present for schema-readiness, wired in a later epic.
    stripe_customer_id text,
    subscription_status text,
    plan text,
    current_period_end timestamp with time zone,
    trial_end timestamp with time zone
);

-- One membership+role model (AD-2). The `shadchan` role is reserved in
-- the check from day one but granted nothing in v1 RLS (deny posture).
-- Replaces the fork's `sales` concept for the shidduchim domain; `sales`
-- itself is left in place for the legacy CRM resources.
create table public.account_members (
    id bigint generated by default as identity primary key,
    account_id bigint not null,
    created_at timestamp with time zone not null default now(),
    user_id uuid,
    role text not null default 'parent_admin',
    status text not null default 'active',
    invited_by bigint,
    constraint account_members_role_check check (
        role in ('parent_admin', 'helper', 'self_manager', 'shadchan')
    )
);

