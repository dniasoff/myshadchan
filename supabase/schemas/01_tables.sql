-- Redt history: a shidduch can be redt (proposed) many times, by the same
-- shadchan or different ones, on different dates (FR13 refinement). Each row is
-- one redt event. shidduchim.redt_date (= the LAST redt), shidduchim.shadchan_id
-- (= the latest redt's shadchan, the card "via"), and first_suggested_by/at
-- (= the earliest redt) are DERIVED from this table by the
-- refresh_shidduch_redt_summary() trigger — never hand-set as the source of truth.
create table public.redts (
    id bigint generated by default as identity primary key,
    account_id bigint not null,
    created_at timestamp with time zone not null default now(),
    shidduchim_id bigint not null,
    shadchan_id bigint,
    redt_date date not null default current_date,
    note text
);

-- Education history: a single can have attended several institutions — a
-- seminary (girl) or yeshiva (boy), plus schools/college — each with optional
-- years. shidduchim.seminary_en/_he stays the card "headline"; this table holds
-- the full list. `kind` distinguishes seminary vs yeshiva vs school.
create table public.shidduch_schools (
    id bigint generated by default as identity primary key,
    account_id bigint not null,
    created_at timestamp with time zone not null default now(),
    shidduchim_id bigint not null,
    kind text not null default 'seminary',
    name_en text,
    name_he text,
    start_year integer,
    end_year integer,
    constraint shidduch_schools_kind_check check (
        kind in ('seminary', 'yeshiva', 'school', 'college', 'other')
    )
);

-- Transitions-as-data (AD-4, invariant 2): the legal pipeline_state edges,
-- enforced by public.transition_shidduch() and the enforce_pipeline_transition
-- trigger — never scattered ifs. Rows are seeded by the migration.
create table public.pipeline_transitions (
    from_state public.pipeline_state not null,
    to_state public.pipeline_state not null,
    constraint pipeline_transitions_pkey primary key (from_state, to_state)
);

