-- Reusable references (schema shape only in this epic; the References UI
-- and reference matching/merge logic are Epic-3/Epic-4). A reference is
-- reusable within an account and links across many shidduchim via
-- reference_links. Name/phone fields are present so a future
-- matchIdentity()/matchOnEntry can dedupe — no matching logic is built now.
-- NOTE: `references` is a reserved SQL keyword, so the table name is always
-- double-quoted in raw SQL (public."references"). PostgREST exposes it as the
-- `references` resource; the frontend never sees the quoting.
create table public."references" (
    id bigint generated by default as identity primary key,
    account_id bigint not null,
    created_at timestamp with time zone not null default now(),
    name_en text,
    name_he text,
    relationship text,
    phone text,
    school text,
    grad_year integer
);

-- The central object (AD-4). One child, one canonical pipeline_state.
-- Sole INSERT path = public.create_shidduch(); sole state writer =
-- public.transition_shidduch(). See 02_functions.sql.
create table public.shidduchim (
    id bigint generated by default as identity primary key,
    account_id bigint not null,
    created_at timestamp with time zone not null default now(),
    child_id bigint not null,
    shadchan_id bigint,
    -- Single's identity (bilingual, AD-12)
    name_en text,
    name_he text,
    -- Name-bearing context fields, bilingual where they carry a name
    parents_en text,
    parents_he text,
    seminary_en text,
    seminary_he text,
    shul_en text,
    shul_he text,
    location_en text,
    location_he text,
    -- Informational only — NEVER matching signals (FR11)
    age integer,
    height text,
    -- The one canonical state (AD-4); NO decision_substate (D4)
    pipeline_state public.pipeline_state not null default 'new',
    -- Provenance (FR13): first shadchan to redt + when
    first_suggested_by bigint,
    first_suggested_at timestamp with time zone not null default now(),
    -- Board-visible "Redt {date}"
    redt_date date not null default current_date,
    -- Set when entering a terminal state
    close_reason text,
    -- 'channel' | 'manual' | 'shadchan' (shadchan reserved for Phase-2)
    origin text not null default 'manual',
    -- Visibility (AD-3) — columns locked NOW so the deferred child portal
    -- needs no breaking migration. owner_member_id is the visibility owner.
    owner_member_id bigint,
    visibility text not null default 'shared',
    -- Per-column ordering on the board (mirrors deals.index)
    index smallint not null default 0,
    constraint shidduchim_origin_check check (origin in ('channel', 'manual', 'shadchan')),
    constraint shidduchim_visibility_check check (
        visibility in ('shared', 'private_parent', 'private_child')
    )
);

