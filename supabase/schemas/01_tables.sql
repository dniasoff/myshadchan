-- =====================================================================
-- MyShadchan — Shidduchim pipeline domain (AD-1, AD-4, AD-12)
-- =====================================================================
-- The central object is a `shidduch` (plural `shidduchim`): a proposed
-- match/single a shadchan redts. Every row carries a non-null account_id
-- (tenant isolation, AD-1) and is RLS-scoped by account. Bilingual
-- identity fields store both scripts (`_en`/`_he`, AD-12).
--
-- Full RLS enforcement (FORCE + multi-account current_account_ids +
-- anon-grant revocation + invite binding) is Epic-1 and DEFERRED; this
-- build ships the account_id columns, account-scoped policies, and no
-- anon grants, so RLS can be hardened later WITHOUT a schema change.
--

-- The one canonical pipeline state (AD-4): exactly 7 values, decision
-- states (yes/unsure/no) reachable only from look_into. Gut
-- `for_sure_not` is deliberately distinct from post-investigation `no`.
-- There is NO separate decision_substate — everything is this enum (D4).
create type public.pipeline_state as enum (
    'new',
    'look_into',
    'not_sure',
    'for_sure_not',
    'yes',
    'unsure',
    'no'
);

-- Tenant root. Also carries billing state (AD-16) — billing columns are
-- schema-ready but unused in this build.
