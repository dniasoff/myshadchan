--
-- Foreign keys (shidduchim domain)
--
-- (account_id, id) uniqueness is what makes the composite foreign keys below
-- possible. Every FK that crosses between two tenant-owned tables carries
-- account_id, so a row can only ever reference a parent in its own account.
-- These are all ON DELETE CASCADE, which is what made the single-column version
-- dangerous: one tenant deleting their own child would have cascaded into
-- another tenant's shidduch. Without them a plain FK on shidduchim_id lets one tenant point a row
-- at another tenant's shidduch: no read leak, but it plants rows naming a
-- stranger's records and turns FK-violation-versus-success into a cross-tenant
-- id oracle. The composite FK makes crossing the boundary impossible rather
-- than merely unrewarding.
