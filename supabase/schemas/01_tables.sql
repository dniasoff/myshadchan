-- Billing + AI entitlement (E4). AI (research assistant, future resume
-- auto-parse) is the ONLY paid surface; everything else stays free forever.
--
-- One subscription row per account. The DEFAULT posture is UNENTITLED: no row,
-- or a row that is not exactly (plan='ai', status='active'), locks the AI
-- features. The client may only SELECT this table (05_policies.sql /
-- 06_grants.sql) — there is deliberately NO client-callable path that flips
-- `plan` to 'ai' or `status` to 'active', because that would be exactly the
-- billing bypass the old client-side entitlement placeholder warned about.
-- Every write is service_role (payment webhook / admin), which bypasses RLS.
create table public.subscription (
    id bigint generated by default as identity primary key,
    account_id bigint not null,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now(),
    -- 'free' = the free-forever tier; 'ai' = the paid AI tier ($2/mo · $24/yr).
    plan text not null default 'free',
    -- 'active' = entitled and paid; 'lapsed' = was paid, now expired (AI
    -- auto-fill pauses, the free manual path stays); 'none' = never subscribed.
    status text not null default 'none',
    current_period_end timestamp with time zone,
    constraint subscription_plan_check check (plan in ('free', 'ai')),
    constraint subscription_status_check check (status in ('active', 'lapsed', 'none'))
);

