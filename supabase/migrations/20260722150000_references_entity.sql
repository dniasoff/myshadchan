CREATE OR REPLACE FUNCTION public.sync_task_target()
 RETURNS trigger
 LANGUAGE plpgsql
 SET search_path TO ''
AS $function$
begin
  if coalesce(new.target_type, 'contact') = 'contact' then
    new.target_type := 'contact';
    new.target_id := coalesce(new.target_id, new.contact_id);
    new.contact_id := coalesce(new.contact_id, new.target_id);
  else
    new.contact_id := null;
  end if;

  if new.target_id is null then
    raise exception 'a task needs a target: set contact_id, or target_type + target_id'
      using errcode = 'not_null_violation';
  end if;

  return new;
end;
$function$
;

CREATE OR REPLACE FUNCTION public.current_account_id()
 RETURNS bigint
 LANGUAGE plpgsql
 STABLE SECURITY DEFINER
 SET search_path TO ''
AS $function$
declare
  v_account_id bigint;
begin
  select am.account_id into v_account_id
  from public.account_members am
  where am.user_id = auth.uid()
    and am.status = 'active'
  order by am.id
  limit 1;

  return v_account_id;
end;
$function$
;

CREATE OR REPLACE FUNCTION public.handle_new_user()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO ''
AS $function$
declare
  sales_count int;
  v_account_id bigint;
begin
  select count(id) into sales_count
  from public.sales;

  insert into public.sales (first_name, last_name, email, user_id, administrator)
  values (
    coalesce(new.raw_user_meta_data ->> 'first_name', new.raw_user_meta_data -> 'custom_claims' ->> 'first_name', 'Pending'),
    coalesce(new.raw_user_meta_data ->> 'last_name', new.raw_user_meta_data -> 'custom_claims' ->> 'last_name', 'Pending'),
    new.email,
    new.id,
    case when sales_count > 0 then FALSE else TRUE end
  );

  if not exists (select 1 from public.account_members) then
    select a.id into v_account_id
    from public.accounts a
    order by a.id
    limit 1;

    if v_account_id is null then
      insert into public.accounts (name) values ('My Account')
      returning id into v_account_id;
    end if;

    insert into public.account_members (account_id, user_id, role, status)
    values (v_account_id, new.id, 'parent_admin', 'active');
  end if;

  return new;
end;
$function$
